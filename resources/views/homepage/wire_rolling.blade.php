@section('machine_description')
<p>Rolling mills (pictured) are used to thin metal, and taper rods. Rolling mills have parallel hard steel rollers, which are used to thin metal sheet or wire by rotating rollers, forcing the material through. The distance between the rollers can be adjusted. Rolling wire is idea to shape metal to desired thicknesses prior to forming jewelry. Mills must be bolted down, either to sturdy bench surfaces, or to a purpose-built stand. They require constant cleaning and oiling so that the mechanisms and surfaces of the rollers stay in working order.</p>
