@section('machine_description')
<p>It is necessary to soften metal before bending it - this makes the task much easier and reduces the risk of damaging the metal through the use of excessive force. Techniques such as forging, where the metal is being physically deformed, will quickly work-harden it and so regular annealing is necessary. Metal becomes harder due to internal stresses, and heating it to a specific temperatures relieves these stresses and softens the metal.</p>
<p>Metals are most often joined together using the soldering process. Solder is a metal alloy with a lower melting point than the metals it is joining; precious metals each have their own solder alloy, but base metals are usually joined with a silver solder. Solder machines (pictured) increase the rate of soldering individual pieces, otherwise done by hand.</p>
